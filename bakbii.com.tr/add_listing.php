if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $title = trim($_POST['title']);
    $description = trim($_POST['description']);
    $price = trim($_POST['price']);
    $address = trim($_POST['address']);
    $phone_number = trim($_POST['phone_number']);
    $property_type = trim($_POST['property_type']);

    if (empty($title) || empty($description) || empty($price) || empty($address) || empty($phone_number) || empty($property_type)) {
        $error = 'Tüm alanları doldurunuz.';

    } elseif (!is_numeric($price) || $price <= 0) {
        $error = 'Geçerli bir fiyat giriniz.';
    } elseif (empty($_FILES['photos']['name'][0])) {
        $error = 'En az bir fotoğraf yüklemelisiniz.';
    } else {
        try {
            $conn->beginTransaction();
            
            // Insert property listing
            $stmt = $conn->prepare("INSERT INTO property_listings (user_id, title, description, price, address, phone_number, property_type) VALUES (?, ?, ?, ?, ?, ?, ?)");
            $stmt->execute([$_SESSION['user_id'], $title, $description, $price, $address, $phone_number, $property_type]);
            $property_id = $conn->lastInsertId();

            // Handle image uploads
            $upload_dir = 'uploads/';
            if (!file_exists($upload_dir)) {
                mkdir($upload_dir, 0777, true);
            }

            $uploaded_files = 0;
            foreach ($_FILES['photos']['tmp_name'] as $key => $tmp_name) {
                if ($_FILES['photos']['error'][$key] === 0) {
                    $file_extension = pathinfo($_FILES['photos']['name'][$key], PATHINFO_EXTENSION);
                    $new_filename = uniqid() . '.' . $file_extension;
                    $destination = $upload_dir . $new_filename;

                    if (move_uploaded_file($tmp_name, $destination)) {
                        $stmt = $conn->prepare("INSERT INTO property_images (property_id, image_path, is_primary) VALUES (?, ?, ?)");
                        $is_primary = ($uploaded_files === 0) ? true : false;
                        $stmt->execute([$property_id, $destination, $is_primary]);
                        $uploaded_files++;
                    }
                }
            }

            if ($uploaded_files > 0) {
                $conn->commit();
                $success = 'İlan başarıyla eklendi!';
            } else {
                throw new Exception('Fotoğraf yüklenirken bir hata oluştu.');
            }
        } catch(Exception $e) {
            $conn->rollBack();
            $error = 'İlan eklenirken bir hata oluştu: ' . $e->getMessage();
        }
    }
}
